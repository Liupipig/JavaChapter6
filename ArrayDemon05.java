package com.itheima.Array;

public class ArrayDemon05 {
    public static void main(String[] args) {
        String [] name = new String[50];
        name[0]="刘胖";
        name[1]="米斯特儿杨";
        System.out.println(name[0]);

//        数组默认初始化值的规律
//          整数类型:默认初始化值为0；
//          小数类型:默认初始化值为0.0；
//          字符类型:默认初始化值为'/u0000'；
//          Boolean类型:默认初始化值为false；
//          应用数据类型:默认初始化值为null；
    }
}
